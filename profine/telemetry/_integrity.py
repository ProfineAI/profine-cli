# Set this to skip the check entirely (used in tests).
_DISABLE_ENV_VAR = "PROFINE_DISABLE_INTEGRITY_CHECK"


def _telemetry_dir() -> Path:
    return Path(__file__).resolve().parent


def _hash_file(path: Path) -> str | None:
    """sha256 of file bytes; None if file is missing."""
    try:
        return hashlib.sha256(path.read_bytes()).hexdigest()
    except OSError:
        return None


def verify_integrity() -> list[str]:
    """Compare on-disk file hashes to the baked manifest.

    Returns a list of human-readable mismatch descriptions. Empty list
    means "everything matches the manifest" (or "no manifest set; skip").
    """
    if not MANIFEST:
        # No manifest baked in — skip check (dev builds or unbuilt source).
        return []

    if os.environ.get(_DISABLE_ENV_VAR, "").strip().lower() in ("1", "true", "yes", "on"):
        return []

    mismatches: list[str] = []
    base = _telemetry_dir()

    # Check declared files
    for relname, expected in MANIFEST.items():
        path = base / relname
        actual = _hash_file(path)
        if actual is None:
            mismatches.append(f"  MISSING  {relname}")
        elif actual != expected:
            mismatches.append(f"  MODIFIED {relname}  (expected {expected[:12]}..., got {actual[:12]}...)")

    # Detect files we didn't expect (could be added by a modifier).
    on_disk = {
        p.name for p in base.iterdir()
        if p.is_file() and p.suffix == ".py" and p.name != "_integrity.py"
    }
    for unexpected in sorted(on_disk - set(MANIFEST)):
        mismatches.append(f"  EXTRA    {unexpected}")

    return mismatches


def _emit_warning(mismatches: list[str]) -> None:
    """One warning block to stderr. Format is deliberately ugly so it
    doesn't blend into normal output — if you see this, the modules
    you're running aren't what we shipped."""
    sys.stderr.write(
        "\n"
        "⚠️  profine telemetry integrity check failed.\n"
        "    You're running a modified version of profine.telemetry.\n"
        "    Profine support may decline to help with bugs in modified clients.\n"
    )
    for line in mismatches:
        sys.stderr.write(line + "\n")
    sys.stderr.write(
        "    To silence: regenerate the manifest "
        f"(or unset {_DISABLE_ENV_VAR}=1 to disable the check).\n\n"
    )


# Run the check at import time. The result is also exposed via the
# `INTEGRITY_OK` module attribute so callers can branch on it (e.g.
# to mark the telemetry payload as "from a modified client").
_mismatches = verify_integrity()
INTEGRITY_OK: bool = not _mismatches
if _mismatches:
    _emit_warning(_mismatches)



"""Telemetry-module tamper evidence.

What this is
------------
A build-time manifest of sha256 hashes for every file in
`profine/telemetry/`. At import time we recompute the hashes of the
currently-loaded files and compare. A mismatch emits a single warning
line to stderr — it does NOT refuse to run.

What this is NOT
----------------
Tamper *prevention*. We can't stop anyone with Apache 2.0 source from
modifying these files; that's the price of open source. What we can
do is make a modification visible: anyone running modified code sees
a warning printed to stderr on every invocation, and a future
backend can correlate the manifest hash in their telemetry payload
to identify modified clients.

Two assumptions
---------------
1. `MANIFEST` is regenerated by `scripts/rebuild_telemetry_manifest.py`
   before each release. The manifest is committed; PRs that change
   telemetry files MUST regenerate it (and the CI sync check verifies).
2. The verifier itself is removable. A determined modifier patches
   this file too. That's fine — the goal is *evidence*, not control.

Performance
-----------
We hash files once per process at import time (~1ms for the whole
module). Result is cached on the module object; subsequent imports
no-op.
"""

